const isoToFlag = (iso: string): string => {
  return iso
    .toUpperCase()
    .split("")
    .map((char) => String.fromCodePoint(char.charCodeAt(0) + 127397))
    .join("");
};

export const DEFAULT_COUNTRIES: Country[] = RAW_COUNTRIES.map((country, index) => ({
  ...country,
  flag: isoToFlag(country.iso2),
  priority: index
}));

export const DEFAULT_COUNTRY_MAP: Record<string, Country> = Object.fromEntries(
  DEFAULT_COUNTRIES.map((country) => [country.iso2, country])
);

export type CountryLookup = typeof DEFAULT_COUNTRY_MAP;
